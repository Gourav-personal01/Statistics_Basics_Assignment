#Q2. Define the different types of statistics and give an example of when each type might be used.

# Aanswer - 

# There are Two types of Statistics - 1. Discriptive stats and 2. Inferential Stats.

# 1. Discriptive Stats - This is used to Sumerizing and analysing the data.
# Example - Mean,Median,Std,variance

# 2. Inferential Stats - This is used to detemine the conclusion of a data(Population) by a sample data.
# Example - Z test, t test, chi-square etc